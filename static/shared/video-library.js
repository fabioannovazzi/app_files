  function updateFeaturedVideo(featured) {
    const link = document.querySelector("[data-featured-video]");
    const image = document.querySelector("[data-featured-video-thumb]");
    const duration = document.querySelector("[data-featured-video-duration]");
    if (!link || !image || !duration) return;
    link.hidden = !featured;
    if (!featured) return;

    link.href = `https://youtu.be/${featured.id}`;
    image.src = thumbnailUrl(featured.id);
    duration.textContent = featured.duration;
  }

  function buildGuide(video, openLabel) {
    const link = document.createElement("a");
    link.className = "video-guide";
    link.href = `https://youtu.be/${video.id}`;
    link.target = "_blank";
    link.rel = "noopener noreferrer";
    link.setAttribute("aria-label", `${video.shortTitle || video.title}. ${openLabel}`);
    if (video.module) link.dataset.videoModule = video.module;
    if (video.workstream) link.dataset.videoWorkstream = video.workstream;
    if (video.edition) link.dataset.videoEdition = video.edition;
    if (video.scope) link.dataset.videoScope = video.scope;
    if (video.jurisdiction) link.dataset.videoJurisdiction = video.jurisdiction;

    const thumbnail = document.createElement("span");
    thumbnail.className = "video-guide__thumb";
    thumbnail.setAttribute("aria-hidden", "true");

    const image = document.createElement("img");
    image.src = thumbnailUrl(video.id);
    image.alt = "";
    image.width = 1280;
    image.height = 720;
    image.loading = "lazy";

    const play = document.createElement("span");
    play.className = "video-guide__play";
    play.textContent = "▶";

    const body = document.createElement("span");
    body.className = "video-guide__body";

    if (video.moduleLabel) {
      const module = document.createElement("span");
      module.className = "video-guide__module";
      module.textContent = video.moduleLabel;
      body.append(module);
    }

    const title = document.createElement("strong");
    title.textContent = video.shortTitle || stripProductPrefix(video.title);

    const description = document.createElement("span");
    description.className = "video-guide__description";
    description.textContent = video.description || "";

    const meta = document.createElement("span");
    meta.className = "video-guide__meta";
    meta.textContent = [video.duration || "", openLabel].filter(Boolean).join(" · ");

    thumbnail.append(image, play);
    body.append(title);
    if (video.description) body.append(description);
    body.append(meta);
    link.append(thumbnail, body);
    return link;
  }

