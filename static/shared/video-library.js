  function normalizeVideo(product, lang, video) {
    if (product !== "vera") {
      const audioLanguage = video.audioLanguage || lang;
      return {
        ...video,
        sourceKind: "youtube",
        kind: "guide",
        language: lang,
        audioLanguage,
        shortTitle: stripProductPrefix(video.title),
        lastVerifiedAt
      };
    }

    const identity = veraYoutubeIdentityById.get(video.id);
    const audioLanguage = video.audioLanguage || lang;
    if (!identity || identity.language !== audioLanguage) {
      throw new Error(`Missing Vera video identity for ${lang}/${video.id}`);
    }
    const { module, edition, scope, jurisdiction, jurisdictions, kind } = identity;
    const [moduleLabel, defaultDescription] = veraModuleCopy[lang][module];
    const description = identity.descriptions?.[lang] || defaultDescription;
    return {
      ...video,
      sourceKind: "youtube",
      module,
      edition,
      scope,
      jurisdiction,
      ...(jurisdictions ? { jurisdictions } : {}),
      moduleLabel,
      workstream: veraWorkstream[module],
      kind,
      language: lang,
      audioLanguage,
      shortTitle: stripProductPrefix(video.title),
      description,
      duration: video.duration || null,
      status: "published",
      pageTargets: identity.pageTargets || veraPageTargets[module],
      lastVerifiedAt
    };
  }

